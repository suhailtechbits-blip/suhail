
import { ConstituencyData, Front, TargetCategory } from './types';

// Helper to determine target category based on margin and winner
const calculateCategory = (winner: Front, margin: number): TargetCategory => {
  if (winner === 'UDF') {
    return margin > 5000 ? 'Safe' : 'Swing';
  } else if (winner === 'LDF' || winner === 'NDA') {
    return margin < 5000 ? 'Swing' : 'Weak';
  }
  return 'Unknown';
};

const createSeat = (
  id: number, 
  name: string, 
  district: string, 
  totalVoters: number, 
  winner: Front, 
  candidateName: string, 
  party: string,
  margin: number
): ConstituencyData => {
  
  // Calculate 2026 targets
  const votes2021Estimate = Math.floor(totalVoters * 0.75 / (winner === 'UDF' ? 2 : 2.2)); // Rough estimate for demo
  
  let targetVotes2026 = 0;
  if (winner === 'UDF') {
      targetVotes2026 = Math.floor(votes2021Estimate * 1.05); // Aim for 5% growth
  } else {
      targetVotes2026 = Math.floor(votes2021Estimate + margin + (totalVoters * 0.02)); // Cover margin + buffer
  }

  const requiredGrowth = ((targetVotes2026 - votes2021Estimate) / votes2021Estimate) * 100;

  return {
    id: id.toString().padStart(3, '0'),
    name,
    district,
    totalVoters,
    verifiedVoters: 0,
    projectedTurnout: 75,
    lastElectionWinner: winner,
    winningMargin: margin,
    targetCategory: calculateCategory(winner, margin),
    status: 'Pending',
    candidates: [
      { id: `c-${id}-1`, name: candidateName, front: winner, partyName: party },
      { id: `c-${id}-2`, name: "Opposition Candidate", front: winner === 'UDF' ? 'LDF' : 'UDF', partyName: winner === 'UDF' ? 'LDF Party' : 'UDF Party' }
    ],
    issues: [],
    votes2021: votes2021Estimate,
    targetVotes2026,
    requiredGrowth: parseFloat(requiredGrowth.toFixed(2))
  };
};

